    def setSelectionType(self, selectionType):
        """Changes the selectors between dropdowns and button grids.

        :param selectionType: The name of the environment the selector should
            apply for. Currently it only checks for "SCORHE" as input, matching
            it with dropdown, and assumes anything else is a button grid.
        :type selectionType: str
        :return: Nothing
        :rtype: None
        """
        if selectionType.lower() == "scorhe":
            self.topRightSelect = QtWidgets.QComboBox(self.connectedCages)
            self.gridLayout.addWidget(self.topRightSelect, 1, 2, 1, 1)
            self.topLeftSelect = QtWidgets.QComboBox(self.connectedCages)
            self.gridLayout.addWidget(self.topLeftSelect, 1, 0, 1, 1)
            self.botLeftSelect = QtWidgets.QComboBox(self.connectedCages)
            self.gridLayout.addWidget(self.botLeftSelect, 3, 0, 1, 1)
            self.botRightSelect = QtWidgets.QComboBox(self.connectedCages)
            self.gridLayout.addWidget(self.botRightSelect, 3, 2, 1, 1)
        else:
            self.topRightSelect, buttonGrid = self.generateButtons(self.connectedCages)
            self.gridLayout.addLayout(buttonGrid, 1, 2, 1, 1)
            self.topLeftSelect, buttonGrid = self.generateButtons(self.connectedCages)
            # self.topLeftSelect.setEditable(True)
            self.gridLayout.addLayout(buttonGrid, 1, 0, 1, 1)
            self.botLeftSelect, buttonGrid = self.generateButtons(self.connectedCages)
            # self.botLeftSelect.setEditable(True)
            self.gridLayout.addLayout(buttonGrid, 3, 0, 1, 1)
            self.botRightSelect, buttonGrid = self.generateButtons(self.connectedCages)
            # self.botRightSelect.setEditable(True)
            self.gridLayout.addLayout(buttonGrid, 3, 2, 1, 1)
        self.cameraPickers.update({"botRightSelect": self.botRightSelect, "topLeftSelect": self.topLeftSelect,
                                   "topRightSelect": self.topRightSelect, "botLeftSelect": self.botLeftSelect})

    @staticmethod
    def generateButtons(parent):
        """Generates a grid of buttons for selection, along with a dict for easy access.

        The dict maps the "name" or "coordinate" for a camera to its associated
        button.

        :param parent: The parent the buttons and QGridLayout should be attached
            to.
        :type parent: QWidget
        :return: A tuple of the map for accessing the buttons, and the
            QGridLayout.
        :rtype: Tuple[Dict[str, QtWidgets.QToolButton], QtWidgets.QGridLayout]
        """
        buttonMap = {}
        grid = QtWidgets.QGridLayout(parent)
        sizePolicy = QtWidgets.QSizePolicy(QtWidgets.QSizePolicy.Minimum, QtWidgets.QSizePolicy.Minimum)
        
        for r, rl in enumerate(["A", "C", "E", "G"]):
            text = QtWidgets.QLabel(rl)
            grid.addWidget(text, r+1, 0, 1, 1)
            for c, cl in enumerate(["01", "03", "05", "07", "09", "11"]):
                if r == 0:
                    colText = QtWidgets.QLabel(cl)
                    grid.addWidget(colText, 0, c+1, 1, 1)
                buttonMap[rl+cl] = QtWidgets.QToolButton(parent)
                buttonMap[rl+cl].setSizePolicy(sizePolicy)
                buttonMap[rl+cl].setEnabled(False)
                grid.addWidget(buttonMap[rl+cl], r+1, c+1, 1, 1)
        return buttonMap, grid

